int		ft_atoi(const char *s)
{
	int sign;
	int count;
	int c;

	sign = 1;
	count = 0;
	c = 0;
	while ((*s == '\r') || (*s == '\n') || (*s == '\t') || (*s == '\v') ||
			(*s == '\f') || (*s == ' ') || (*s == '0'))
		s++;
	if (*s == '-' || *s == '+')
		if (*s++ == '-')
			sign = -1;
	while (*s >= 48 && *s <= 57)
	{
		c = (c * 10) + (*s - '0');
		s++;
		count++;
	}
	if (count < 20)
		return (c * sign);
	else if (sign != -1)
		return (-1);
	return (0);
}
