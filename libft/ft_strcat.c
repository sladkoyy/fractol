char	*ft_strcat(char *dest, const char *app)
{
	int i;
	int c;

	i = 0;
	c = 0;
	while (dest[i] != '\0')
		i++;
	while (app[c] != '\0')
	{
		dest[i] = app[c];
		i++;
		c++;
	}
	dest[i] = '\0';
	return (dest);
}
