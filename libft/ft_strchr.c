#include <stdlib.h>

char	*ft_strchr(const char *s1, int c)
{
	char	*p;
	int		i;

	i = 0;
	p = NULL;
	while (s1[i] != '\0')
	{
		if (s1[i] == c)
		{
			p = ((char*)s1 + i);
			return (p);
		}
		i++;
	}
	if (c == '\0' && s1[i] == '\0')
		p = ((char*)s1 + i);
	return (p);
}
