#include <stdio.h>

int		ft_strlenc(char *str, int c)
{
	int		i;

	i = 0;
	if (str[0] == '\0')
		return (0);
	while (str[i] != '\0')
	{
		if (str[i] == c)
			return (i);
		i++;
	}
	return (-1);
}
